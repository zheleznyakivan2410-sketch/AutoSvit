// Виконуємо скрипт, коли DOM-структура сторінки повністю завантажена
$(document).ready(function(){

  // --- 1. Ініціалізація слайдера відгуків (Owl Carousel) ---
  // Перевіряємо, чи існує елемент з класом .reviews-slider на сторінці
  if ($('.reviews-slider').length) {
    // Якщо так, ініціалізуємо для нього Owl Carousel
    $('.reviews-slider').owlCarousel({
      loop: true, // Зациклюємо слайдер
      margin: 30, // Відстань між слайдами в пікселях
      nav: true, // Вмикаємо навігаційні стрілки (вперед/назад)
      dots: false, // Вимикаємо навігаційні точки
      // Налаштування адаптивності: кількість видимих слайдів залежно від ширини екрану
      responsive:{
          0:{ // Для екранів від 0px і ширше
              items:1 // Показувати 1 слайд
          },
          768:{ // Для екранів від 768px і ширше
              items:2 // Показувати 2 слайди
          },
          992:{ // Для екранів від 992px і ширше
              items:3 // Показувати 3 слайди
          }
        }
    });
  }

  // --- 2. Логіка для мобільного гамбургер-меню ---
  // Знаходимо елементи кнопки-гамбургера та навігаційного меню
  const hamburger = document.querySelector('.hamburger-menu');
  const nav = document.querySelector('.main-nav');

  // Перевіряємо, чи обидва елементи існують на сторінці
  if (hamburger && nav) {
    // Додаємо обробник події 'click' на кнопку-гамбургер
    hamburger.addEventListener('click', function() {
      // При кліку перемикаємо клас 'active' для кнопки та навігації,
      // що викликає їх показ/приховування та анімацію (задано в CSS)
      hamburger.classList.toggle('active');
      nav.classList.toggle('active');
    });
  }

});