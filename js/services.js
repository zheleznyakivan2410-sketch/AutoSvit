// Виконуємо скрипт, коли DOM-структура сторінки повністю завантажена
$(document).ready(function(){

    // --- Ініціалізація слайдера "Як це працює?" (Owl Carousel) ---

    // Перевіряємо, чи існує на сторінці елемент з класом .steps-slider
    if ($('.steps-slider').length) {
      // Якщо елемент знайдено, ініціалізуємо для нього плагін Owl Carousel з налаштуваннями
      $('.steps-slider').owlCarousel({
        loop: true,       // Зациклює слайдер, щоб після останнього слайду йшов перший.
        margin: 30,       // Відстань між слайдами в пікселях.
        nav: true,        // Вмикає навігаційні стрілки (вперед/назад).
        dots: false,      // Вимикає навігаційні точки під слайдером.
        // Налаштування адаптивності: кількість видимих слайдів залежно від ширини екрану.
        responsive:{
            0:{               // Для екранів від 0px і ширше
                items:1       // показувати 1 слайд.
            },
            768:{             // Для екранів від 768px і ширше
                items:2       // показувати 2 слайди.
            }
        }
      });
    }
});